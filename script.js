const clickSound = new Audio('https://assets.mixkit.co/active_storage/sfx/2568/2568-preview.mp3'); // Short click sound

function playClickSound() {
    clickSound.currentTime = 0; // Reset to start if already playing
    clickSound.play().catch(error => console.log("Audio play blocked by browser:", error));
}

